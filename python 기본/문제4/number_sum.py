# 1. 합계를 저장할 변수 초기화
total = 0

# 2. 조건이 참인 동안 계속 반복 (True를 쓰면 무한 반복)
while True:
    # 3. 사용자로부터 숫자 입력받기
    num = int(input("숫자를 입력하세요 (0을 입력하면 종료): "))
    
    # 4. 0이 입력되면 반복문 종료
    if num == 0:
        break
    
    # 5. 0이 아니면 합계에 누적
    total += num

# 6. 결과 출력
print(f"입력된 숫자들의 합: {total}")